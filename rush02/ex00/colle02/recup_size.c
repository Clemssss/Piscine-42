int		nb_car(char *buf)
{
	int		i;

	i = 0;
	while (buf[i] != '\0' && buf[i] != '\n')
		i++;
	return (i);
}

int		nb_line(char *buf)
{
	int		i;
	int		len;

	i = 0;
	len = 0;
	while (buf[i] != '\0')
	{
		if (buf[i] == '\n')
			len++;
		i++;
	}
	return (len);
}
