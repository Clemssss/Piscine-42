#include "ft.h"

int		sub(int a, int b)
{
	return (a - b);
}

int		add(int a, int b)
{
	return (a + b);
}

int		mul(int a, int b)
{
	return (a * b);
}

int		div(int a, int b)
{
	return (a / b);
}

int		mod(int a, int b)
{
	return (a % b);
}
