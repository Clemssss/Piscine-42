int		ft_count_if(char **tab, int (*f)(char*))
{
	int		i;
	int		len;

	len = 0;
	i = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			len++;
		i++;
	}
	return (len);
}
